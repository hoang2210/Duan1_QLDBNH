    void InsertMonAn() {
        CTHoaDon1 model = getModel();
        for (CTHoaDon1 x : lst) {
            String MaHD = String.valueOf(x.getMaHD() + "");
            String tenmon = x.getTenMon();
            String trangthai = x.getTrangThaiMon();
            if (MaHD.equals(txt_MaHD.getText()) && tenmon.equals(lb_TenMon.getText()) && trangthai.equals("Chưa Lên")) {
                DialogHelper.alert(this, "Món đã được gọi trước đó");
                return;
            }
        }
        try {
            CTHDDao.insert(model);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

