    void fillTable() {
        try {
            String sql = "Select *\n"
                    + "from ((HoaDon inner join PhieuDat on HoaDon.MaPD=PhieuDat.MaPD)\n"
                    + "inner join ThongTinKH on ThongTinKH.MaKH = PhieuDat.MaKH)\n"
                    + "WHERE MaHD =?";
            ResultSet rs = JdbcHelper.executeQuery(sql, MaHD);
            while (rs.next()) {
                txt_TenKhach.setText(rs.getString("Hoten"));
                txt_SoDT.setText(rs.getString("SDT"));
                txt_SoNguoi.setText(String.valueOf(rs.getInt("SoNguoi")));
                txt_NgayAn.setText(String.valueOf(rs.getDate("DateBook")));

                MaPH = rs.getInt("MaPD");
                txt_MaHD.setText(String.valueOf(rs.getInt("MaHD")));
            }
        } catch (SQLException e) {
            e.printStackTrace();

        }
    }

    void fillComboBoxLoaiMon() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cbb_LoaiMon.getModel();
        model.removeAllElements();
        try {
            List<LoaiMon> listLM = LMdao.select();
            if (!listLM.isEmpty()) {
                for (LoaiMon cd : listLM) {
                    model.addElement(cd);
                }
            }

        } catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }

    private void LoadTableMonAn(String MaLoaiMon) {
        model.setRowCount(0);
        List<MonAn> lst = MAdao.getAll(MaLoaiMon);
        for (int i = 0; i < lst.size(); i++) {
            MonAn s = lst.get(i);
            Object[] obj = new Object[]{
                s.getMaMon(),
                s.getTenMon(),
                s.getGiaTien(),
                s.getGioiThieu(),};
            model.addRow(obj);
        }
    }

    private void LoadTableMeNu(int MaHD) {
        model1.setRowCount(0);
        lst = CTHDDao.getAll(MaHD);
        for (int i = 0; i < lst.size(); i++) {
            CTHoaDon1 s = lst.get(i);
            Object obj[] = new Object[7];

            obj[0] = model1.getRowCount() + 1;
            obj[1] = s.getTenMon();
            obj[2] = s.getGiaTien();
            obj[3] = s.getSoLuong();
            obj[4] = Double.parseDouble(s.getSoLuong() + "") * s.getGiaTien();
            obj[5] = s.getGhiChu();
            obj[6] = s.getTrangThaiMon();
            model1.addRow(obj);

        }
    }

