    BanAn getModel(){
        BanAn model = new BanAn();
        model.setSoBan(Integer.parseInt(txtSoBan.getText()));
        if (rdoHD.isSelected()) {
            model.setTrangThaiHD("Đang hoạt động");
        } else{
            model.setTrangThaiHD("Tu sửa");
        }
        return model;
    }
    void setModel(){
        BanAn model = new BanAn();
        txtSoBan.setText(String.valueOf(model.getSoBan()));
        if (model.getTrangThaiHD().equals("Đang hoạt động")) {
            rdoHD.setSelected(true);
        } else {
            rdoTuSua.setSelected(true);
        }
    }
    
    void Load(){
        DefaultTableModel model = (DefaultTableModel) tblBan.getModel();
        model.setRowCount(0);
        try {
            List<BanAn> list = dao.select();
            for (BanAn x : list) {
                Object[] row = {
                    x.getSoBan(),
                    x.getTrangThaiHD()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
        }
    }
    void Insert(){       
        try {           
            if (txtSoBan.getText().equals("")) {
                DialogHelper.alert(this, "Số bàn không được để trống ");
                return;
            }   
            BanAn model = getModel();
            dao.insert(model);
            DialogHelper.alert(this, "Thêm bàn thành công");
            Load();
            txtSoBan.setText("");
        } catch (Exception e) {
            DialogHelper.alert(this, "Đã tồn tại số bàn này");
        }
    }


    
    void Update() {
        BanAn model = getModel();
        try {
            dao.update(model);
            DialogHelper.alert(this, "Cập nhật thành công");
            txtSoBan.setText("");
            Load();
        } catch (Exception e) {
            DialogHelper.alert(this, "Cập nhật thất bại");
        }
    }
    public void DodulieulenComponent(int row) {
        if (row < 0) {
            return;
        }
        txtSoBan.setText(tblBan.getValueAt(row, 0).toString());
        String trangthai = tblBan.getValueAt(row, 1).toString();
        if (trangthai.equalsIgnoreCase("Đang hoạt động")){
            rdoHD.setSelected(true);
        } else {
            rdoTuSua.setSelected(true);
        }

    }
