    private void txt_TaiKhoanFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txt_TaiKhoanFocusGained
    txt_TaiKhoan.addFocusListener(new FocusListener() {
            @Override
            public void focusGained(FocusEvent e) {
                if (txt_TaiKhoan.getText().equals("Tài khoản")) {
                    txt_TaiKhoan.setText("");
                    txt_TaiKhoan.setForeground(Color.BLACK);
                } else {
                    txt_TaiKhoan.selectAll();
                }
            }

            @Override
            public void focusLost(FocusEvent e) {
                if (txt_TaiKhoan.getText().equals("")) {
                    txt_TaiKhoan.setText("Tài khoản");
                    txt_TaiKhoan.setForeground(Color.GRAY);
                }
            }
        });
    }//GEN-LAST:event_txt_TaiKhoanFocusGained

