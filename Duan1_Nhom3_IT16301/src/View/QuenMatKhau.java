    private  void QuenMK(){
        String us = txt_TaiKhoan.getText();
        String phone = txt_Phone.getText();
        try {
            KhachHang kh = dao.findById(us);
            if (kh != null) {
                String phone2 = kh.getSdt();
                if (phone.equals(phone2)) {
                    update();
                }else{
                    DialogHelper.alert(this, "Sai số điện thoại");
                }
            }else{
               DialogHelper.alert(this, "Sai tài khoản ");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    TaiKhoanKH getModel(){
        int len = 6;
        String kitu = "1234567890";
//        qwertyuiopasdfghjklmnbvcxzQWERTYUIOPLKJHGFDSAZXCVBNM
        StringBuilder st = new StringBuilder();
        Random rd = new Random();
        while (0<len--) {            
            st.append(kitu.charAt(rd.nextInt(kitu.length())));
        }
        TaiKhoanKH model = new TaiKhoanKH();
        model.setUsername(txt_TaiKhoan.getText());
        model.setPass(st.toString());
        DialogHelper.alert(this,"Mật khẩu mới của bạn là:" +st.toString());
        return model;
    }
    void update(){
        TaiKhoanKH model =getModel();
        try {
            tkkh.update(model);
        } catch (Exception e) {
        }
    }
